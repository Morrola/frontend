// بيانات افتراضية للتقييمات
const reviews = [
  { id: 1, userName: "Ali", review: "Great product!", date: "2024-11-10" },
  { id: 2, userName: "Sara", review: "Satisfactory service", date: "2024-11-10" }
];

// تحميل التقييمات على الصفحة
function loadReviews() {
  const reviewTable = document.querySelector("#review-table tbody");
  reviewTable.innerHTML = reviews.map(review => `
      <tr>
          <td>${review.userName}</td>
          <td>${review.review}</td>
          <td>${review.date}</td>
          <td>
              <button onclick="acceptReview(${review.id})">Accept</button>
              <button onclick="rejectReview(${review.id})">Reject</button>
          </td>
      </tr>
  `).join("");
}

// قبول التقييم
function acceptReview(id) {
  alert(`Review ID ${id} accepted.`);
  // قم بإضافة كود هنا لإرسال القبول للخادم
}

// رفض التقييم
function rejectReview(id) {
  alert(`Review ID ${id} rejected.`);
  // قم بإضافة كود هنا لإرسال الرفض للخادم
}

// تحميل التقييمات عند تحميل الصفحة
document.addEventListener("DOMContentLoaded", loadReviews);
